const baseLocale = require('locales/base.json') as { [name: string]: string };

interface LocWithReplace {
    with(value: string): string;
}

let activeLocale = baseLocale;
let localeImpl: { [name: string]: string | LocWithReplace } = {};

function set(values: { [name: string]: string } | undefined): void {
    if (values) {
        activeLocale = {};
        for (const [key, baseLocaleValue] of Object.entries(baseLocale)) {
            const value = values[key] ?? baseLocaleValue;
            activeLocale[key] = value;
            if (typeof localeImpl[key] === 'string') {
                localeImpl[key] = value;
            }
        }
    } else {
        activeLocale = baseLocale;
        for (const [key, value] of Object.entries(baseLocale)) {
            if (typeof localeImpl[key] === 'string') {
                localeImpl[key] = value;
            }
        }
    }
}

function str(name: string): string {
    return activeLocale[name] ?? '';
}

function get(name: string): string | undefined {
    return activeLocale[name];
}

function strWithReplace(name: string): LocWithReplace {
    return { with: (repl) => str(name).replace('{}', repl) };
}

